                   PSS((SPIN+3)/2)=PSS((SPIN+3)/2)+(1.0/(Z1*LX*LY))*GK1(K1,K2)*(H0(K1,K2,INTOP,SPIN)/H(K1,K2,INTOP,SPIN))*&
                    &(BEC_DIS(ENERGY(K1,K2,INTOP,SPIN,ALPHA),TEMP)+BEC_DIS(ENERGY(K1,K2,INTOP,-1*SPIN,BETA),TEMP)+1)

                ENDDO
            ENDDO
        ENDDO
END SUBROUTINE

