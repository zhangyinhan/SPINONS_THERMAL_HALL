        REAL(KIND=RKIND),INTENT(IN)::MEDTOP(OPNUM),FINTOP(OPNUM)
        REAL(KIND=RKIND),INTENT(OUT)::AVAL
        INTEGER::I1
        AVAL=0.0D0
        DO I1=1,OPNUM
            AVAL=AVAL+(FINTOP(I1)-MEDTOP(I1))**2
        ENDDO
        AVAL=SQRT(AVAL/10)
        PRINT*,"AVAL=",AVAL
END SUBROUTINE

